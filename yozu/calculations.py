import math

def sin(x):
    return math.sin(math.radians(x))


def cos(x):
    return math.cos(math.radians(x))


def tan(x):
    return math.tan(math.radians(x))


def ln(x):
    return math.log2(math.radians(x))


def log(x):
    return math.log10(x)


def fact(x):
    return math.factorial(x)


def root(x):
    return math.sqrt(x)


def pow(x, y):
    return math.pow(x, y)

